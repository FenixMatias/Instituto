    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
       String rut,nombre;
       double notapresentacion;
       rut=txtRut.getText();
       nombre=txtNombre.getText();
       notapresentacion=Double.parseDouble(txtNota.getText());
       Alumno stu=new Alumno(rut, nombre, notapresentacion);
       //JOptionPane.showMessageDialog(this, man.ingresaralumno(student),"Ingreso Alumno",JOptionPane.INFORMATION_MESSAGE);
       AlumnoDAO stuDAO=new AlumnoDAO();
       if(stuDAO.buscaralumno(rut)==null)
       {
           stuDAO.ingresaralumno(stu);
           JOptionPane.showMessageDialog(this, "Alumno Ingresado");
       }
       else
           JOptionPane.showMessageDialog(this, "El Alumno ya existe");
       
    }//GEN-LAST:event_btnIngresarActionPerformed

