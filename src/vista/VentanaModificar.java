    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String rut=txtRut.getText();
        AlumnoDAO aluDAO=new AlumnoDAO();
        Alumno alu=aluDAO.buscaralumno(rut);
        if(alu==null)
        {
           JOptionPane.showMessageDialog(this, "Alumno no Existe");
        }
        else
        {
            pnlDatos.setVisible(true);
            txtRut1.setText(rut);
            txtNombre.setText(alu.getNombre());
            txtNota.setText(String.valueOf(alu.getNotapresentacion()));
            
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

