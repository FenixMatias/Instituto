    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        AlumnoDAO aluDAO=new AlumnoDAO();
        Alumno alu=new Alumno(txtRut.getText(),txtNombre.getText(),Double.parseDouble(txtNota.getText()));
        if(aluDAO.modificaralumno(alu))
        {
            JOptionPane.showMessageDialog(this, "Datos Modificados");
            pnlDatos.setVisible(false);
            txtRut.setText(null);
            txtRut.requestFocus();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "No se Puede Modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

